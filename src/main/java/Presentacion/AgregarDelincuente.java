    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean Cond1 = jDateChooser1.getDate() != null;
        boolean Cond2 = jDateChooser2.getDate() != null;
        if ((jTextField1.getText().equals("")) || (jTextField2.getText().equals("")) || (jTextField3.getText().equals("")) || (jTextField4.getText().equals("")) || !Cond1 || !Cond2 ) {
             JOptionPane.showMessageDialog(null, "Llene todos los campos debidamente", "Mensaje", JOptionPane.WARNING_MESSAGE);
        } else {
            String code = "";
            Random rnd = new Random();
            int n = 999 - 100 + 1;
            int i = rnd.nextInt() * n;
            String[] nombre = jTextField1.getText().split(" ");
            for (int j = 0; j < nombre.length; j++) {
                code = code + nombre[j].charAt(0);
            }
            code = code + i;
            Preso nP = new Preso(code, jTextField5.getText(), jTextField1.getText(),jTextField2.getText(), jDateChooser1.getDate(),Integer.valueOf(jTextField3.getText()), OR.getCode(jComboBox3.getSelectedIndex()), DL.getCode(jComboBox2.getSelectedIndex()), jDateChooser2.getDate(), Integer.valueOf(jTextField4.getText()),AG.getCode(jComboBox1.getSelectedIndex()),0);
            CR.AgregarCriminal(nP);
            clear();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

