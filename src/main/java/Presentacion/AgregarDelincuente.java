    public void cargar() {
        DefaultComboBoxModel list1 = new DefaultComboBoxModel();
        DefaultComboBoxModel list2 = new DefaultComboBoxModel();
        DefaultComboBoxModel list3 = new DefaultComboBoxModel();
        
        for(Policia ag : AG.cargarAgentes()) {
            list1.addElement(ag.getNombre());
        }
        for(Delito dl : DL.cargarDelitos()) {
            list2.addElement(dl.getName());
        }
        for(Grupo_Criminal or : OR.cargarOrganizaciones()) {
            list3.addElement(or.getNombre());
        }
        jComboBox1.setModel(list1);
        jComboBox2.setModel(list2);
        jComboBox3.setModel(list3);
    }

